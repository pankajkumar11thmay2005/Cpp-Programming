#include <iostream>
using namespace std;

int main() {
  int a = 6;

  cout<<a<<endl;

  a = a + 4;  // a = 6 + 4 = 10
  cout<<a<<endl;

  a = a - 2;  // a = 10 - 2 = 8
  cout<<a<<endl;

  a = 5;
  cout<<a<<endl;

  a = a * 3;  // a = 5 * 3 = 15
  cout<<a<<endl;

  a = a / 5;  // a = 15 / 5 = 3
  cout<<a<<endl;

  a = a % 2;  // a = 3 % 2 = 1
  cout<<a<<endl;

  a += 4;  // a = a + 4 = 1 + 4 = 5
  cout<<a<<endl;

  a -= 2;  // a = a - 2 = 5 - 2 = 3
  cout<<a<<endl;

}